// write stuff on USB CDC device
int cdc_write(uint8_t *buffer, unsigned int len) {
	return CDC_Transmit_FS(buffer, len);
}

// read stuff from USB CDC device
// return 0 if no data is available
int cdc_read(uint8_t *buffer, unsigned int max_len) {
	int bytes_rx = 0;
	uint8_t *byte_ptr;
	// read bytes of data from ring buffer until empty, or max_len reached.
	while( (byte_ptr = rb_get(&cdc_rx_rb) ) && (bytes_rx <= max_len) ) {
		buffer[bytes_rx] = *byte_ptr;
		++bytes_rx;
	}
	return bytes_rx;
}

int cdc_data_pending() {
	if ( rb_isempty(&cdc_rx_rb) ) {
		// no data available to read
		return 0;
	} else {
		// data available to read
		return 1;
	}
}

uint8_t to_upper(uint8_t b) {
	if ( ( b >= 97 ) && ( b <= 122 ) ) {
		return b - 32;
	} else {
		return b;
	}
}

int is_usb_connected() {
	if ( hUsbDeviceFS.dev_state == USBD_STATE_CONFIGURED) {
		return 1;
	} else {
		return 0;
	}
}


